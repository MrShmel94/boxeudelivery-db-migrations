COMMENT ON TABLE crm.border_tariff_category IS
    'Administrator-managed two-level classification used by cross-border tariff policy.';
COMMENT ON TABLE crm.border_tariff_subcategory IS
    'Stable selectable goods subcategories. Used rows are deactivated rather than deleted.';
COMMENT ON COLUMN crm.supplier_goods_entry.border_tariff_subcategory_id IS
    'Classification inherited by every exact physical unit in this reusable supplier-goods entry.';
COMMENT ON TABLE crm.border_tariff_policy IS
    'Versioned tariff policy. Published versions are immutable; one draft is edited for the next publication.';
COMMENT ON TABLE crm.border_tariff_policy_exchange_rate IS
    'Policy-owned conversion snapshot expressed as USD for one unit of the named purchase currency.';
COMMENT ON TABLE crm.cargo_item_border_tariff_calculation IS
    'Immutable per-item tariff calculation snapshot linked to the exact purchase-cost revision.';
