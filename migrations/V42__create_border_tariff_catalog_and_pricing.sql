CREATE TABLE crm.border_tariff_category
(
    id                 UUID         NOT NULL,
    code               VARCHAR(64)  NOT NULL,
    display_name       VARCHAR(150) NOT NULL,
    active             BOOLEAN      NOT NULL DEFAULT TRUE,
    display_order      INTEGER      NOT NULL DEFAULT 0,
    created_by_subject VARCHAR(255) NOT NULL,
    updated_by_subject VARCHAR(255) NOT NULL,
    created_at         TIMESTAMPTZ  NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at         TIMESTAMPTZ  NOT NULL DEFAULT CURRENT_TIMESTAMP,
    version            BIGINT       NOT NULL DEFAULT 0,
    CONSTRAINT pk_border_tariff_category PRIMARY KEY (id),
    CONSTRAINT uq_border_tariff_category_code UNIQUE (code),
    CONSTRAINT uq_border_tariff_category_name UNIQUE (display_name),
    CONSTRAINT ck_border_tariff_category_code CHECK (code ~ '^[A-Z][A-Z0-9_]{1,63}$'),
    CONSTRAINT ck_border_tariff_category_name CHECK (BTRIM(display_name) <> ''),
    CONSTRAINT ck_border_tariff_category_order CHECK (display_order >= 0),
    CONSTRAINT ck_border_tariff_category_actor CHECK (
        BTRIM(created_by_subject) <> '' AND BTRIM(updated_by_subject) <> ''
    ),
    CONSTRAINT ck_border_tariff_category_timestamps CHECK (updated_at >= created_at),
    CONSTRAINT ck_border_tariff_category_version CHECK (version >= 0)
);

CREATE TABLE crm.border_tariff_subcategory
(
    id                 UUID         NOT NULL,
    category_id        UUID         NOT NULL,
    code               VARCHAR(64)  NOT NULL,
    display_name       VARCHAR(150) NOT NULL,
    active             BOOLEAN      NOT NULL DEFAULT TRUE,
    display_order      INTEGER      NOT NULL DEFAULT 0,
    created_by_subject VARCHAR(255) NOT NULL,
    updated_by_subject VARCHAR(255) NOT NULL,
    created_at         TIMESTAMPTZ  NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at         TIMESTAMPTZ  NOT NULL DEFAULT CURRENT_TIMESTAMP,
    version            BIGINT       NOT NULL DEFAULT 0,
    CONSTRAINT pk_border_tariff_subcategory PRIMARY KEY (id),
    CONSTRAINT uq_border_tariff_subcategory_code UNIQUE (code),
    CONSTRAINT uq_border_tariff_subcategory_name UNIQUE (category_id, display_name),
    CONSTRAINT fk_border_tariff_subcategory_category
        FOREIGN KEY (category_id) REFERENCES crm.border_tariff_category (id) ON DELETE RESTRICT,
    CONSTRAINT ck_border_tariff_subcategory_code CHECK (code ~ '^[A-Z][A-Z0-9_]{1,63}$'),
    CONSTRAINT ck_border_tariff_subcategory_name CHECK (BTRIM(display_name) <> ''),
    CONSTRAINT ck_border_tariff_subcategory_order CHECK (display_order >= 0),
    CONSTRAINT ck_border_tariff_subcategory_actor CHECK (
        BTRIM(created_by_subject) <> '' AND BTRIM(updated_by_subject) <> ''
    ),
    CONSTRAINT ck_border_tariff_subcategory_timestamps CHECK (updated_at >= created_at),
    CONSTRAINT ck_border_tariff_subcategory_version CHECK (version >= 0)
);

CREATE INDEX ix_border_tariff_subcategory_category
    ON crm.border_tariff_subcategory (category_id, active, display_order, display_name, id);

ALTER TABLE crm.supplier_goods_entry
