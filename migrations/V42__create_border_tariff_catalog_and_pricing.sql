    ADD CONSTRAINT fk_supplier_goods_entry_border_tariff_subcategory
        FOREIGN KEY (border_tariff_subcategory_id)
            REFERENCES crm.border_tariff_subcategory (id) ON DELETE RESTRICT;

CREATE INDEX ix_supplier_goods_entry_border_tariff_subcategory
    ON crm.supplier_goods_entry (border_tariff_subcategory_id, project_id, created_at DESC, id)
    WHERE border_tariff_subcategory_id IS NOT NULL;

CREATE TABLE crm.border_tariff_policy
(
    id                    UUID          NOT NULL,
    policy_number         BIGINT GENERATED BY DEFAULT AS IDENTITY,
    status_code           VARCHAR(16)   NOT NULL,
    percentage_rate       NUMERIC(9, 6) NOT NULL,
    automatic_limit_eur   NUMERIC(19,4) NOT NULL,
    created_by_subject    VARCHAR(255)  NOT NULL,
    updated_by_subject    VARCHAR(255)  NOT NULL,
    published_by_subject  VARCHAR(255),
    created_at            TIMESTAMPTZ   NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at            TIMESTAMPTZ   NOT NULL DEFAULT CURRENT_TIMESTAMP,
    published_at          TIMESTAMPTZ,
    version               BIGINT        NOT NULL DEFAULT 0,
    CONSTRAINT pk_border_tariff_policy PRIMARY KEY (id),
    CONSTRAINT uq_border_tariff_policy_number UNIQUE (policy_number),
    CONSTRAINT ck_border_tariff_policy_status
        CHECK (status_code IN ('DRAFT', 'PUBLISHED', 'ARCHIVED')),
    CONSTRAINT ck_border_tariff_policy_percentage
        CHECK (percentage_rate > 0 AND percentage_rate <= 1),
    CONSTRAINT ck_border_tariff_policy_limit CHECK (automatic_limit_eur > 0),
    CONSTRAINT ck_border_tariff_policy_actor CHECK (
        BTRIM(created_by_subject) <> ''
        AND BTRIM(updated_by_subject) <> ''
        AND (published_by_subject IS NULL OR BTRIM(published_by_subject) <> '')
    ),
    CONSTRAINT ck_border_tariff_policy_publication CHECK (
        (status_code = 'DRAFT' AND published_by_subject IS NULL AND published_at IS NULL)
        OR (status_code IN ('PUBLISHED', 'ARCHIVED') AND published_by_subject IS NOT NULL AND published_at IS NOT NULL)
    ),
    CONSTRAINT ck_border_tariff_policy_timestamps CHECK (
        updated_at >= created_at AND (published_at IS NULL OR published_at >= created_at)
    ),
    CONSTRAINT ck_border_tariff_policy_version CHECK (version >= 0)
);

CREATE UNIQUE INDEX uq_border_tariff_policy_one_draft
    ON crm.border_tariff_policy (status_code)
    WHERE status_code = 'DRAFT';

CREATE UNIQUE INDEX uq_border_tariff_policy_one_published
    ON crm.border_tariff_policy (status_code)
    WHERE status_code = 'PUBLISHED';

CREATE TABLE crm.border_tariff_policy_minimum
(
    policy_id      UUID          NOT NULL,
    subcategory_id UUID          NOT NULL,
    minimum_usd    NUMERIC(19,4) NOT NULL,
    CONSTRAINT pk_border_tariff_policy_minimum PRIMARY KEY (policy_id, subcategory_id),
    CONSTRAINT fk_border_tariff_policy_minimum_policy
        FOREIGN KEY (policy_id) REFERENCES crm.border_tariff_policy (id) ON DELETE CASCADE,
    CONSTRAINT fk_border_tariff_policy_minimum_subcategory
        FOREIGN KEY (subcategory_id) REFERENCES crm.border_tariff_subcategory (id) ON DELETE RESTRICT,
    CONSTRAINT ck_border_tariff_policy_minimum_amount CHECK (minimum_usd >= 0)
);

CREATE INDEX ix_border_tariff_policy_minimum_subcategory
    ON crm.border_tariff_policy_minimum (subcategory_id, policy_id);

CREATE TABLE crm.border_tariff_policy_exchange_rate
(
    policy_id       UUID           NOT NULL,
    currency_code   VARCHAR(3)     NOT NULL,
    usd_per_unit    NUMERIC(24,10) NOT NULL,
    CONSTRAINT pk_border_tariff_policy_exchange_rate PRIMARY KEY (policy_id, currency_code),
    CONSTRAINT fk_border_tariff_policy_exchange_rate_policy
        FOREIGN KEY (policy_id) REFERENCES crm.border_tariff_policy (id) ON DELETE CASCADE,
    CONSTRAINT fk_border_tariff_policy_exchange_rate_currency
        FOREIGN KEY (currency_code) REFERENCES crm.currency_definition (code) ON DELETE RESTRICT,
    CONSTRAINT ck_border_tariff_policy_exchange_rate_value CHECK (usd_per_unit > 0),
    CONSTRAINT ck_border_tariff_policy_exchange_rate_usd CHECK (
        currency_code <> 'USD' OR usd_per_unit = 1
    )
);

CREATE TABLE crm.cargo_item_border_tariff_calculation
(
    id                          UUID           NOT NULL,
    cargo_item_id               UUID           NOT NULL,
    project_id                  UUID           NOT NULL,
    subcategory_id              UUID           NOT NULL,
    policy_id                   UUID           NOT NULL,
    purchase_entry_id           UUID           NOT NULL,
    purchase_revision           INTEGER        NOT NULL,
    status_code                 VARCHAR(32)     NOT NULL,
    purchase_amount             NUMERIC(19,4)  NOT NULL,
    purchase_currency_code      VARCHAR(3)      NOT NULL,
    purchase_usd_per_unit       NUMERIC(24,10) NOT NULL,
    eur_usd_per_unit            NUMERIC(24,10) NOT NULL,
    normalized_purchase_usd     NUMERIC(19,4)  NOT NULL,
    normalized_purchase_eur     NUMERIC(19,4)  NOT NULL,
    percentage_rate             NUMERIC(9,6)   NOT NULL,
    percentage_amount_usd       NUMERIC(19,4)  NOT NULL,
    minimum_amount_usd          NUMERIC(19,4)  NOT NULL,
    final_amount_usd            NUMERIC(19,4),
    generated_financial_entry_id UUID,
    supersedes_id               UUID,
    active                      BOOLEAN        NOT NULL DEFAULT TRUE,
    created_by_subject          VARCHAR(255)   NOT NULL,
    created_at                  TIMESTAMPTZ    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT pk_cargo_item_border_tariff_calculation PRIMARY KEY (id),
    CONSTRAINT uq_cargo_item_border_tariff_calculation_scope
        UNIQUE (id, cargo_item_id, project_id),
    CONSTRAINT uq_cargo_item_border_tariff_calculation_supersedes UNIQUE (supersedes_id),
    CONSTRAINT fk_cargo_item_border_tariff_calculation_item
        FOREIGN KEY (cargo_item_id, project_id)
            REFERENCES crm.cargo_item (id, project_id) ON DELETE RESTRICT,
    CONSTRAINT fk_cargo_item_border_tariff_calculation_subcategory
        FOREIGN KEY (subcategory_id) REFERENCES crm.border_tariff_subcategory (id) ON DELETE RESTRICT,
    CONSTRAINT fk_cargo_item_border_tariff_calculation_policy
        FOREIGN KEY (policy_id) REFERENCES crm.border_tariff_policy (id) ON DELETE RESTRICT,
    CONSTRAINT fk_cargo_item_border_tariff_calculation_purchase_revision
        FOREIGN KEY (purchase_entry_id, purchase_revision, cargo_item_id, project_id)
            REFERENCES crm.cargo_item_financial_revision (
                financial_entry_id, revision_number, cargo_item_id, project_id
            ) ON DELETE RESTRICT,
    CONSTRAINT fk_cargo_item_border_tariff_calculation_purchase_currency
        FOREIGN KEY (purchase_currency_code) REFERENCES crm.currency_definition (code) ON DELETE RESTRICT,
    CONSTRAINT fk_cargo_item_border_tariff_calculation_generated_entry
        FOREIGN KEY (generated_financial_entry_id)
            REFERENCES crm.cargo_item_financial_entry (id) ON DELETE RESTRICT,
    CONSTRAINT fk_cargo_item_border_tariff_calculation_supersedes
        FOREIGN KEY (supersedes_id)
            REFERENCES crm.cargo_item_border_tariff_calculation (id) ON DELETE RESTRICT,
    CONSTRAINT ck_cargo_item_border_tariff_calculation_status
        CHECK (status_code IN ('AUTO_CALCULATED', 'MANUAL_REVIEW_REQUIRED', 'MANUALLY_PRICED')),
    CONSTRAINT ck_cargo_item_border_tariff_calculation_purchase CHECK (
        purchase_revision >= 1
        AND purchase_amount >= 0
        AND purchase_usd_per_unit > 0
        AND eur_usd_per_unit > 0
        AND normalized_purchase_usd >= 0
        AND normalized_purchase_eur >= 0
    ),
    CONSTRAINT ck_cargo_item_border_tariff_calculation_formula CHECK (
        percentage_rate > 0 AND percentage_rate <= 1
        AND percentage_amount_usd >= 0
        AND minimum_amount_usd >= 0
    ),
    CONSTRAINT ck_cargo_item_border_tariff_calculation_result CHECK (
        (
            status_code = 'AUTO_CALCULATED'
            AND final_amount_usd IS NOT NULL
            AND final_amount_usd >= minimum_amount_usd
            AND generated_financial_entry_id IS NOT NULL
        )
        OR (
            status_code IN ('MANUAL_REVIEW_REQUIRED', 'MANUALLY_PRICED')
            AND final_amount_usd IS NULL
        )
    ),
    CONSTRAINT ck_cargo_item_border_tariff_calculation_actor
        CHECK (BTRIM(created_by_subject) <> '')
);

CREATE UNIQUE INDEX uq_cargo_item_border_tariff_calculation_active
    ON crm.cargo_item_border_tariff_calculation (cargo_item_id)
    WHERE active;

CREATE INDEX ix_cargo_item_border_tariff_calculation_policy
    ON crm.cargo_item_border_tariff_calculation (policy_id, created_at DESC, id);

CREATE INDEX ix_cargo_item_border_tariff_calculation_manual_review
    ON crm.cargo_item_border_tariff_calculation (project_id, created_at DESC, cargo_item_id)
    WHERE active AND status_code = 'MANUAL_REVIEW_REQUIRED';

